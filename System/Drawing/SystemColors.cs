using System;

namespace System.Drawing
{
    public sealed class SystemColors
    {
        public static Color AppWorkspace { get { return Color.FromArgb(171, 171, 171); } }
        public static Color Control { get { return Color.FromArgb(245, 245, 245); } }
        public static Color Highlight {  get { return Color.FromArgb(51, 153, 255); } }
        public static Color HighlightText { get { return Color.FromArgb(255, 255, 255); } }
    }
}
